class MixinLog:
    """
    Миксин для логирования создания экземпляров классов.
    Выводит в консоль информацию о том, какой объект и с какими параметрами был создан.
    """

    def __init__(self):
        """
        Метод __init__ в миксине не принимает параметров, кроме self,
        потому что он будет вызван через super() и не должен мешать основной инициализации.
        """
        print(repr(self))

    def __repr__(self):
        """
        Возвращает строковое представление объекта с именем класса и аргументами.
        Используется для отображения при создании.
        """
        args = ", ".join(f"{value!r}" for value in self.__dict__.values())
        return f"{self.__class__.__name__}({args})"
